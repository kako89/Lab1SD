    private void BotonIngresoBiblioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIngresoBiblioActionPerformed
        // TODO add your handling code here:
        try {
                //Intentamos conectarnos con el servidor
                //Si hay exito empezamos a consumir servicios
                if (conexion.iniciarRegistry()){
                    if (conexion.getServidor().RegistrarBiblioteca(this.NombreBiblio.getText().toLowerCase(), this.DireccionBiblio.getText().toLowerCase())){
                        JOptionPane.showMessageDialog(this, "Biblioteca Registrada", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                        vistaAgregarBiblioteca VAB=new vistaAgregarBiblioteca();
                        VAB.setLocationRelativeTo(null);
                        VAB.setVisible(true);
                        //conexion.registrarCliente(Nombre);

                    }
                    else{
                        JOptionPane.showMessageDialog(this, "No se pudo registrar biblioteca", "Mensaje", JOptionPane.ERROR_MESSAGE);
                        
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se pudo Conectar", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }

            } catch (RemoteException ex) {
                Logger.getLogger(vistaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_BotonIngresoBiblioActionPerformed

