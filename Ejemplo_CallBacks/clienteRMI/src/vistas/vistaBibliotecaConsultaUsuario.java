    private void ListadoBibliosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListadoBibliosActionPerformed
        // TODO add your handling code here:

        String seleccionado=(String)ListadoBiblios.getSelectedItem();

        if (contador==0){
            contador++;
        }
        else{
            System.out.println(seleccionado);
            contador++;

            try {
                //Intentamos conectarnos con el servidor
                //Si hay exito empezamos a consumir servicios
                if (conexion.iniciarRegistry()){
                    String DatosBiblio=new String();
                    DatosBiblio=conexion.getServidor().MostrarBiblio(seleccionado);
                    String[] resultadoConsulta=DatosBiblio.split(";");
                    
                    if(!resultadoConsulta[0].equals("")){
                    SectorMuestra.setText("\nNombre Biblioteca: " + seleccionado + "\n" + "Dirección: " + resultadoConsulta[1]);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "No hay bibliotecas en el sistema", "Mensaje", JOptionPane.ERROR_MESSAGE);
                    }
                    

                }
                else{
                    JOptionPane.showMessageDialog(this, "No se pudo Conectar", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }

                //String seleccionado=(String)ListadoBiblios.getSelectedItem();

            } catch (RemoteException ex) {
                Logger.getLogger(vistaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_ListadoBibliosActionPerformed

